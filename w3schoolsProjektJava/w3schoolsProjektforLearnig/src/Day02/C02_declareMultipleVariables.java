package Day02;

public class C02_declareMultipleVariables {
    public static void main (String [] args) {
        int x = 5 ;
        int y = 6 ;
        int z = 50 ;
        System.out.println ( x + y + z ) ;

        int a = 5 , b = 6 , c = 50 ;
        System.out.println ( a + b + c ) ;

        int ax , by , cz ;
        ax = by = cz = 50 ;
        System.out.println ( ax + by + cz ) ;


    }

}
