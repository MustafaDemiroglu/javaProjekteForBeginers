package Day02;

public class C03_javaDataTypes {
    public static void main ( String [ ] args ) {
        int myNum = 5 ;                 // integer (whole number)
        float myFloatNum = 5.99f ;      // floating point number
        char myLetter = 'D' ;           // character
        boolean myBool = true ;         // boolean ( true or false)
        String myText = "Hello" ;       // String (all Texts)
        System.out.println (myNum + " integer (whole number)" ) ;
        System.out.println (myFloatNum + " floating point number" ) ;
        System.out.println (myLetter + " character" ) ;
        System.out.println (myBool + " boolean ( true or false)" ) ;
        System.out.println (myText + " String (all Texts)" ) ;
    }
}
